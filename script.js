/* ── Counter Animation ───────────────────────────────────────────────── */
function animateCounter(el) {
  const original = el.textContent.trim();
  // Match patterns like "120+", "45+", "150%", "3.2x"
  const match = original.match(/^(\d+(?:\.\d+)?)([+%x]*)$/);
  if (!match) return; // Skip "24/7" etc.

  const target   = parseFloat(match[1]);
  const isFloat  = match[1].includes(".");
  const suffix   = match[2] || "";
  const duration = 1800;
  const start    = performance.now();

  function step(now) {
    const elapsed  = now - start;
    const progress = Math.min(elapsed / duration, 1);
    // Ease-out quart
    const eased    = 1 - Math.pow(1 - progress, 4);
    const current  = eased * target;
    el.textContent = (isFloat ? current.toFixed(1) : Math.round(current)) + suffix;
    if (progress < 1) requestAnimationFrame(step);
  }

  requestAnimationFrame(step);
}

function initCounters() {
  const counterEls = document.querySelectorAll(".stat-card strong");
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          animateCounter(entry.target);
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.5 }
  );
  counterEls.forEach((el) => observer.observe(el));
}

/* ── Case Study Card ─────────────────────────────────────────────────── */
function createCaseCard(study, index) {
  const isFeatured = index === 0;
  const card = document.createElement("article");
  card.className = `portfolio-card${isFeatured ? " portfolio-card--featured" : ""}`;
  card.tabIndex = 0;
  card.setAttribute("role", "button");
  card.setAttribute(
    "aria-label",
    `${study.result[currentLang]} — ${study.title[currentLang]}`
  );

  const videoId = `video-${study.title.en.toLowerCase().replace(/\s+/g, "-")}`;

  card.innerHTML = `
    <div class="portfolio-card__media">
      <video muted loop playsinline preload="metadata" id="${videoId}">
        <source src="${study.videoUrl}" type="video/mp4" />
      </video>
      <div class="portfolio-card__overlay">
        <span class="portfolio-card__play" aria-hidden="true"></span>
        <span class="portfolio-card__eyebrow">${study.category[currentLang]}</span>
        <h3 class="portfolio-card__title">${study.result[currentLang]}</h3>
      </div>
    </div>
    <div class="portfolio-card__body">
      <span class="portfolio-card__result">${study.category[currentLang]}</span>
      <h3>${study.title[currentLang]}</h3>
      <p>${study.description[currentLang]}</p>
    </div>
  `;

  const previewVideo = card.querySelector("video");
  card.addEventListener("mouseenter", () => previewVideo.play().catch(() => {}));
  card.addEventListener("mouseleave", () => {
    previewVideo.pause();
    previewVideo.currentTime = 0;
  });
  card.addEventListener("focus", () => previewVideo.play().catch(() => {}));
  card.addEventListener("blur",  () => {
    previewVideo.pause();
    previewVideo.currentTime = 0;
  });

  const open = () => openModal(study);
  card.addEventListener("click", open);
  card.addEventListener("keydown", (event) => {
    if (event.key === "Enter" || event.key === " ") {
      event.preventDefault();
      open();
    }
  });

  return card;
}

function renderCaseStudies() {
  portfolioGrid.innerHTML = "";
  caseStudies.forEach((study, index) => {
    portfolioGrid.appendChild(createCaseCard(study, index));
  });
  // Apply stagger to the newly rendered cards
  staggerChildren(portfolioGrid);
}

/* ── Modal ───────────────────────────────────────────────────────────── */
function openModal(study) {
  modalFrame.innerHTML = `
    <video controls autoplay muted playsinline>
      <source src="${study.videoUrl}" type="video/mp4" />
    </video>
  `;

  modalCategory.textContent = study.category[currentLang];
  modalTitle.textContent    = study.title[currentLang];
  modalResult.textContent   = study.result[currentLang];
  modalProblem.textContent  = study.problem[currentLang];
  modalSolution.textContent = study.solution[currentLang];
  modalOutcome.textContent  = study.outcome[currentLang];

  // Reset animation so it re-plays each open
  const dialog = modal.querySelector(".video-modal__dialog");
  dialog.style.animation = "none";
  dialog.offsetHeight; // force reflow
  dialog.style.animation = "";

  modal.classList.add("is-open");
  modal.setAttribute("aria-hidden", "false");
  body.style.overflow = "hidden";

  const mv = modalFrame.querySelector("video");
  if (mv) mv.play().catch(() => {});
}

function closeModal() {
  modal.classList.remove("is-open");
  modal.setAttribute("aria-hidden", "true");
  modalFrame.innerHTML = "";
  body.style.overflow = "";
}

